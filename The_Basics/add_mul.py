#!/usr/bin/python3
""" get 3 numbers from user input, add first two numbers and then multiply total by the third"""


num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
num3 = int(input("Enter third number: "))

sum = num1 + num2
ans = sum * num3
print("The answer is", ans)
